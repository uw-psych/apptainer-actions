echo INPUT_FORCE="${INPUT_FORCE:-}" >>"${GITHUB_ENV}"
echo INPUT_JSON="${INPUT_JSON:-}" >>"${GITHUB_ENV}"
echo INPUT_MOUNT="${INPUT_MOUNT:-}" >>"${GITHUB_ENV}"
echo INPUT_NOTEST="${INPUT_NOTEST:-}" >>"${GITHUB_ENV}"
echo INPUT_SECTION="${INPUT_SECTION:-}" >>"${GITHUB_ENV}"
